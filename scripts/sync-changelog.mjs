import { writeFile } from 'node:fs/promises'
import path from 'node:path'

const OUTPUT_PATH = 'app/(pages)/changelog/data.generated.ts'
const RECENT_COUNT = Number.parseInt(process.env.CORE_CHANGELOG_RECENT_COUNT ?? '5', 10)

function cleanInlineMarkdown(value) {
  return value
    .replace(/\*\*(.*?)\*\*/g, '$1')
    .replace(/`(.*?)`/g, '$1')
    .replace(/\[(.*?)\]\((.*?)\)/g, '$1')
    .trim()
}

function parseHeading(line) {
  const match = line.match(/^## \[([^\]]+)\](.*)$/)
  if (!match) return null

  const version = match[1].trim()
  const rest = match[2].replace(/^\s*-\s*/, '').trim()

  if (!rest) {
    return { version, date: '', title: '' }
  }

  const chunks = rest
    .split(' - ')
    .map((part) => part.trim())
    .filter(Boolean)

  if (chunks.length >= 2) {
    return {
      version,
      date: chunks[0],
      title: chunks.slice(1).join(' - '),
    }
  }

  return { version, date: chunks[0] ?? '', title: '' }
}

function parseReleases(markdown) {
  const lines = markdown.replace(/\r\n/g, '\n').split('\n')
  const headingIndexes = []

  for (let i = 0; i < lines.length; i += 1) {
    if (/^## \[[^\]]+\]/.test(lines[i])) {
      headingIndexes.push(i)
    }
  }

  const releases = []

  for (let i = 0; i < headingIndexes.length; i += 1) {
    const start = headingIndexes[i]
    const end = headingIndexes[i + 1] ?? lines.length
    const heading = parseHeading(lines[start])
    if (!heading) continue

    const sectionLines = lines.slice(start + 1, end)
    const highlights = []
    const paragraphBuffer = []

    for (const rawLine of sectionLines) {
      const line = rawLine.trim()
      if (!line) continue
      if (line.startsWith('## ')) continue
      if (line.startsWith('### ')) continue

      if (line.startsWith('- ')) {
        if (/^- \*\*/.test(line) || /^- [A-Z0-9]/.test(line)) {
          const bullet = cleanInlineMarkdown(line.slice(2))
          if (bullet) highlights.push(bullet)
        }
        continue
      }

      if (!line.match(/^\d+\./)) {
        paragraphBuffer.push(cleanInlineMarkdown(line))
      }
    }

    const summary = paragraphBuffer.join(' ').trim() || heading.title || `Release ${heading.version}`
    const title = heading.title || summary

    releases.push({
      version: heading.version,
      date: heading.date || 'N/A',
      title,
      summary,
      highlights,
    })
  }

  return releases
}

function formatGeneratedTs({ generatedAt, recentReleases, timelineRows }) {
  return `// This file is auto-generated by scripts/sync-changelog.mjs
// Do not edit manually.

export type ReleaseEntry = {
  version: string
  date: string
  title: string
  summary: string
  highlights: string[]
}

export type TimelineRow = {
  version: string
  date: string
  title: string
}

export const changelogGeneratedAt = ${JSON.stringify(generatedAt)}

export const recentReleases: ReleaseEntry[] = ${JSON.stringify(recentReleases, null, 2)}

export const timelineRows: TimelineRow[] = ${JSON.stringify(timelineRows, null, 2)}
`
}

async function readSource() {
  const explicitUrl = process.env.CORE_CHANGELOG_URL

  if (!explicitUrl) {
    throw new Error('CORE_CHANGELOG_URL is required (raw URL to packages/core/CHANGELOG.md).')
  }

  const response = await fetch(explicitUrl)
  if (!response.ok) {
    throw new Error(`Failed to fetch changelog from URL (${response.status} ${response.statusText})`)
  }
  const content = await response.text()
  return { content }
}

async function main() {
  const { content } = await readSource()
  const releases = parseReleases(content)

  if (!releases.length) {
    throw new Error('No release entries were parsed from changelog.')
  }

  const recentReleases = releases.slice(0, RECENT_COUNT).map((release) => ({
    ...release,
    highlights: release.highlights.slice(0, 4),
  }))
  const timelineRows = releases.slice(RECENT_COUNT).map((release) => ({
    version: release.version,
    date: release.date,
    title: release.title,
  }))

  const generated = formatGeneratedTs({
    generatedAt: new Date().toISOString(),
    recentReleases,
    timelineRows,
  })

  await writeFile(path.resolve(OUTPUT_PATH), generated, 'utf8')
  // eslint-disable-next-line no-console
  console.log(`Synced ${releases.length} releases -> ${OUTPUT_PATH} (recent=${recentReleases.length})`)
}

main().catch((error) => {
  // eslint-disable-next-line no-console
  console.error(error instanceof Error ? error.message : String(error))
  process.exit(1)
})
